            template<class Real, int precision>
            class tanh_sinh_detail_constants
            {
               // Arbitary precision constants:
               static Real g(Real const & t)
               {
                  using std::tanh;
                  using std::sinh;
                  return tanh(constants::half_pi<Real>()*sinh(t));
               };
               static Real g_prime(Real const& x)
               {
                  using std::cosh;
                  using std::sinh;
                  return (constants::pi<Real>() * cosh(x)) / (cosh(constants::pi<Real>()* sinh(x)) + 1);
               };
            public:
               static const std::vector<std::vector<Real>> m_abscissas;
               static const std::vector<std::vector<Real>> m_weights;
            };

